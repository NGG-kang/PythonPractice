# 튜플

# 리스트는 [ ]   // 값을 바꿀 수 있다
# 튜플은   ( )    // 값을 바꿀 수 없다
t1 = ( )                #
t1 = (1,)
t2 = 1,2,3
# 1개 요소가 있을땐 마지막에 콤마 필요
# 괄호를 생략해도 무방하다
# 튜플은 거의 사용 안함

# 문자열에서 보았던 t[0](인덱싱)
t1[1:]                   # (슬라이싱)
t1+t2                   # (더하기)
t1*3                    # (곱하기)
len(t1)                 # 길이
# 값을 변경하지 않는 이런 것들은 사용 가능하다
